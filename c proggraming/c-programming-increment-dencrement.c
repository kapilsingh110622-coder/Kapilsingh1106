#include<stdio.h>
int main(){
		int a=5;
		printf("initial value=%d\n",a);
		printf("a++=%d\n",a++); //post increment
		printf("value of a after a++=%d\n",a);
		printf("++a=%d\n",++a); //pre increment
		printf("value of a after ++a=%d\n",a);
		printf("a--=%d\n",a--); //post increment
		printf("value of a after a--=%d\n",a);
		printf("--a=%d\n",--a); //post increment
		printf("value of a after --a=%d\n",a);
		return 0;
}
